    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {
        String joyeriaSeleccionada = jComboBox2.getSelectedItem().toString().toLowerCase();

        List<Encantados> lista_encantados = JoyeriaSelect.recuperarJoyeriaEncantada(joyeriaSeleccionada);
        StringBuilder sb = new StringBuilder();
        DecimalFormat formato = new DecimalFormat("###,###,###");
        for (Encantados encantado : lista_encantados) {
            String nombre = encantado.getNombre();
            sb.append("Nombre: ").append(nombre.substring(0, 1).toUpperCase() + nombre.substring(1).toLowerCase())
                    .append(", nivel: " + encantado.getNivel())
                    .append(", precio: " + formato.format(encantado.getPrecio()))
                    .append(", precisión: " + encantado.getAccuracy())
                    .append(", evasión: " + encantado.getEvasion() + "\n");
        }

        jTextArea1.setText(sb.toString());
    }

