    public void CargarCombo() {

        List<Joyeria> lista_joyeria = JoyeriaSelect.recuperarJoyeria();
        for (Joyeria joyeria : lista_joyeria) {
            jComboBox2.addItem(joyeria.getNombre().toLowerCase());
        }
    }

    public void Actualizarcombo() {
        List<Joyeria> lista_joyeria = JoyeriaSelect.recuperarJoyeria();
        for (Joyeria joyeria : lista_joyeria) {
            boolean existe = false;
            for (int i = 0; i < jComboBox2.getItemCount(); i++) {
                if (jComboBox2.getItemAt(i).equals(joyeria.getNombre().toLowerCase())) {
                    existe = true;
                    break;
                }
            }
            if (!existe) {
                jComboBox2.addItem(joyeria.getNombre().toLowerCase());

            }
        }
    }

    public static void EliminarViejo(String nombreViejo) {
        for (int i = 0; i < jComboBox2.getItemCount(); i++) {
            if (jComboBox2.getItemAt(i).equals(nombreViejo)) {
                jComboBox2.removeItemAt(i);
            }
        }

    }

